package jin.collection.core;

/**
 * Un' operazione da eseguire su un elemento di un'iterazione condividendo un
 * oggetto con le iterazioni precedenti/successive
 * 
 * @author Administrator
 * 
 * TODO To change the template for this generated type comment go to Window -
 * Preferences - Java - Code Style - Code Templates
 */
public interface ChainedOperation {

   /**
    * Riceve l'elemento corrente dell'iterazione (element) e quanto prodotto
    * fino ad ora (currValue). Restituisce un valore che sara' usato come
    * currValue nell'iterazione successiva
    */
   public Object execute(Object element, Object currValue);

}

